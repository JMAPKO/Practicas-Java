    private void cargarTabla() {
        //Defenir el modelo de la tabla
        DefaultTableModel modeloTabla = new DefaultTableModel(){
           //que las filas y las columnas no sean editables
           @Override
           public boolean isCellEditable (int row, int column){
           return false;   
           } 
          
        };
    
       //establecemos nombres alas columnas
       String titulos[] ={"id","usuario","rol"};
       modeloTabla.setColumnIdentifiers(titulos);
       
       //traemos a los usuarios de la base de datos:
       List<Usuario> listaUsuarios = control.traerUsuarios();
       if (listaUsuarios != null){
           for (Usuario usr:listaUsuarios){
               Object[] objeto= {usr.getId(),usr.getNomUsuario(),usr.getUnRol().getNombreRol()};
               
               modeloTabla.addRow(objeto);
               
            }
       }
       
       
       //le asignamos el MODELO a la tabla
       tablaUser.setModel(modeloTabla);
    
    }
    
